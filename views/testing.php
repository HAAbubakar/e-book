    <script>
        document.addEventListener("DOMContentLoaded", function () {
    function autoSlide(sliderClass) {
        const slider = document.querySelector(`.${sliderClass}`);
        const slides = slider.children;
        let visibleSlides, slideWidth, index = 0;

        function updateSlideWidth() {
            const screenWidth = window.innerWidth;

            if (screenWidth >= 1024) {
                visibleSlides = 4; // Desktop: Show 4 slides
            } else if (screenWidth >= 768) {
                visibleSlides = 3; // Tablet: Show 3 slides
            } else {
                visibleSlides = 2; // Mobile: Show 2 slides
            }

            slideWidth = slides[0].offsetWidth + 20; // Adjust for spacing
            slider.style.transform = `translateX(0px)`;
            index = 0;
        }

        function moveSlide() {
            index += visibleSlides; // Move by group of 4, 3, or 2 based on screen size

            if (index >= slides.length) {
                index = 0; // Reset to the first slide when reaching the end
                slider.style.transition = "none";
                slider.style.transform = `translateX(0px)`;
            } else {
                slider.style.transition = "transform 0.5s ease-in-out";
                slider.style.transform = `translateX(-${index * slideWidth}px)`;
            }
        }

        updateSlideWidth();
        window.addEventListener("resize", updateSlideWidth);
        setInterval(moveSlide, 3000); // Auto-slide every 3 seconds
    }

    autoSlide("partnerslider-2");
    autoSlide("partnerslider");
});

    </script>



